/* Refer the 80-P7747-2X Rev.A_sdm660_hardware_register_description.pdf
	FIH_MSM_HW_REV_NUM is mean JTAG_ID(0x00786130) (P.887)
	FIH_MSM_HW_FEATURE_ID is mean QFPROM_CORR_PTE_ROW0_LSB[bits 27:20](0x00784130) (P.843)
	FIH_MSM_SERIAL_NO is mean SERIAL_NUM(0x00786134) (P.2767)
*/
#define FIH_MSM_HW_REV_NUM  0x00786130
#define FIH_MSM_HW_FEATURE_ID  0x00784130
#define FIH_MSM_SERIAL_NO   0x00786134
#define FIH_MSM_HWIO_FEATURE  0x00786024

/*
  Refer the 80-P7747-4 Rev.A_SDM660 DEVICE REVISION GUIDE.pdf
  Refer the 80-P7865-4 Rev.A_SDM630 DEVICE REVISION GUIDE.pdf
  Refer the 80-PD860-4 Rev.B_SDM636 DEVICE REVISION GUIDE.pdf
*/
#define HW_REV_NUM_SDM660	0x0008C0E1
#define HW_REV_NUM_SDM630	0x000AC0E1
#define HW_REV_NUM_SDM636	0x000CC0E1

#define HW_REV_ES 0x00

#endif /* __FIH_CPU_H */
